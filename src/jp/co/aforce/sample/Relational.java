package jp.co.aforce.sample;

public class Relational {
	public static void main(String[] args) {
		int a = 11;
		int b = 3;
		
		System.out.println(a > b);
		System.out.println(a < b);
		System.out.println(a >= b);
		System.out.println(a <= b);
		System.out.println(a == b);
		System.out.println(a != b);
	}
}
